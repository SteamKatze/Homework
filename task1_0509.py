try:
    day_number = int(input("Введіть порядковий номер дня тижня (Ввести число від 1 до 7): "))

    if 1 <= day_number <= 7:
        days = ["понеділок", "вівторок", "середа", "четвер", "п'ятниця", "субота", "неділя"]
        day_name = days[day_number - 1]
        print(f"Назва дня тижня: {day_name}")
    else:
        print("Порядковий номер дня тижня повинен бути від 1 до 7.")
except ValueError:
    print("Введено некоректні дані. Будь ласка, введіть число від 1 до 7.")